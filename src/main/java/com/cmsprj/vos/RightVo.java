package com.cmsprj.vos;

public class RightVo {
	private int grant_no;
	private int right_no;
	private int menu_no;
	private String right_nm;
	public int getGrant_no() {
		return grant_no;
	}
	public void setGrant_no(int grant_no) {
		this.grant_no = grant_no;
	}
	public int getRight_no() {
		return right_no;
	}
	public void setRight_no(int right_no) {
		this.right_no = right_no;
	}
	public int getMenu_no() {
		return menu_no;
	}
	public void setMenu_no(int menu_no) {
		this.menu_no = menu_no;
	}
	public String getRight_nm() {
		return right_nm;
	}
	public void setRight_nm(String right_nm) {
		this.right_nm = right_nm;
	}
}
